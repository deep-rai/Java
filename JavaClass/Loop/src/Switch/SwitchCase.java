package Switch;

public class SwitchCase {
    public void newSwitch(){
        int a=10;
        switch(a){
            case 1:
                System.out.println("One");
                break;
            case 2:
                System.out.println("Two");
                break;
            case 10:
                System.out.println("ten");
                break;
            default:
                System.out.println("Out");
        }

    }
}
